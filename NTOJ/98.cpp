#include <bits/stdc++.h>
using namespace std ;
int main(){
    long long T = 299792458 ;
    cout << "1 " << "Light-second(LS)" <<  " is " << T << " metres." << "\n" ;
    cout << "1 " << "Light-minute(LM)" <<  " is " << T*60 << " metres." << "\n";
    cout << "1 " << "Light-hour(LH)" <<  " is " << T*60*60 << " metres." << "\n"; 
    cout << "1 " << "Light-day(LD)" <<  " is " << T*60*60*24 << " metres." << "\n"; 
    cout << "1 " << "Light-week(LW)" <<  " is " << T*60*60*24*7 << " metres." << "\n"; 
    cout << "1 " << "Light-year(LY)" <<  " is " << T*60*60*24*365 << " metres." << "\n";
}